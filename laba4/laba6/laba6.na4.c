#include <stdio.h>
int main() {
    // Массивы комплексных чисел
    double A[] = {1, 2, 3, 4, 5, 6};      
    double B[] = {6, 5, 4, 3, 2, 1};
    double C[6];  // Массив для суммы 
    // Сложение массивов
    for (int i = 0; i < 6; i++) {
        C[i] = A[i] + B[i];
    }
    // Поиск максимальной реальной части в A
    double max = A[0];  // Первая реальная часть
    for (int i = 0; i < 6; i += 2) {  // Только реальные части
        if (A[i] > max) {
            max = A[i];
        }
    }
    // Подсчёт мнимых частей в B, которые меньше 0
    int cnt = 0;
    for (int i = 1; i < 6; i += 2) {  // Только мнимые части
        if (B[i] < 0) {
            cnt++;
        }
    }
    // Вычисление среднего для реальных и мнимых частей массива C
    double sum_real = 0, sum_mnim = 0;
    for (int i = 0; i < 6; i += 2) {
        sum_real += C[i];      // Сумма реальных частей
        sum_mnim += C[i + 1];  // Сумма мнимых частей
    }
    double real = sum_real / 3;  // Среднее для 3 реальных частей
    double mnim = sum_mnim / 3;  // Среднее для 3 мнимых частей
    printf("Максимальная реальная часть в A: %.2f\n", max);
    printf("Количество мнимых частей в B, меньше 0: %d\n", cnt);
    printf("Среднее реальных частей в C: %.2f\n", real);
    printf("Среднее мнимых частей в C: %.2f\n", mnim);
    return 0;
}
